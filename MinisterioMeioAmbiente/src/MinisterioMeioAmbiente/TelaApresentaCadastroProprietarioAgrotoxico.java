	// Busca os dados do proprietário no Banco de Dados.
	public void PopularJTable() {
		boolean consulta = true;

		try {
			//Driver de conexao com o MySQL.
			Class.forName("com.mysql.jdbc.Driver");

			//Conexão com o Banco de Dados.
			Connection con = DriverManager.getConnection("jdbc:mysql://localhost/ministerio_meio_ambiente", "root", "");

			//Objeto comdo SQL.
			java.sql.Statement stmt = con.createStatement();
			ResultSet RS = null;
			DefaultTableModel model = (DefaultTableModel) tabelaApresentaProprietariosAgrotoxico.getModel();
			model.setNumRows(0);

			//Retorna os dados do Banco de Dados.
			//Aqui faço o select de quatro tabelas cadastro_proprietario_rural, endereco, contato e agrotoxicos..
			RS = stmt.executeQuery(" Select c.*, e.*, con.ddd_telefone_residencial, con.telefone_residencial, con.telefone_celular, con.email, faz_uso, tam_propriedade, agro.herbicida, agro.inseticida, agro.fungicida, agro.bactericida from cadastro_proprietario_rural AS c INNER JOIN endereco AS e ON (c.cpf = e.id_proprietario) INNER JOIN contato AS con ON (c.cpf = con.id_proprietario) INNER JOIN agrotoxicos AS agro ON (c.cpf = agro.id_proprietario) where c.cpf = e.id_proprietario AND c.cpf = con.id_proprietario AND c.cpf = agro.id_proprietario ");
			while (RS.next()) {
				model.addRow(new Object[] {
						//Retorna os dados da tabela do BD, cada campo e um coluna.
						RS.getString("nome"),
						RS.getString("cpf"),
						RS.getString("data_nasc"),
						RS.getString("sexo"),
						RS.getString("logradouro"),
						RS.getString("bairro"),
						RS.getString("cep"),
						RS.getString("numero"),
						RS.getString("municipio"),
						RS.getString("uf"),
						RS.getString("complemento"),
						RS.getString("ddd_telefone_residencial"),
						RS.getString("telefone_residencial"),
						RS.getString("telefone_celular"),
						RS.getString("email"),
						RS.getString("faz_uso"),
						RS.getString("tam_propriedade"),
						RS.getString("herbicida"),
						RS.getString("inseticida"),
						RS.getString("fungicida"),
						RS.getString("bactericida")
				});

				consulta = false;

			}
			
			//Caso os dados não existam, informo ao proprietario que os dados não foram encontrados.
			//Porém isso só acontecerá se não ouver cadastros no Banco de Dados.
			if (consulta) {
				JOptionPane.showMessageDialog(null, "Dados não Encontrados!", "Erro", JOptionPane.ERROR_MESSAGE);

			}

			//Fecha as conexões.
			RS.close();
			stmt.close();
			con.close();

			//Trata os erros de conexão.
		} catch (SQLException Erro) {
			JOptionPane.showMessageDialog(null, "Erro Cmdo SQL" + Erro.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);

		} catch (ClassNotFoundException Erro) {
			JOptionPane.showMessageDialog(null, "Driver não Encontrado!", "Erro", JOptionPane.ERROR_MESSAGE);

		}
	}
